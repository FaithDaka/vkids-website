  <h3 class="mb-30" style="font-size:25px; text-transform:uppercase; text-decoration-line:underline;">Why Kalangala District?</h3>
    <p>Among Ugandan districts, Kalangala is <em>really</em> under-developed and has one of the highest populations of under privileged people in the country, especially children. 
    We believe that every child has a right to a quality and comfortable life. <br />
    And since children on these Islands lack access to basic resources such as nutrition, hygiene, healthcare, education information and safe shelter;
    the organisation had to start.
    </p>
  <h3 class="mb-30" style="font-size:25px; text-transform:uppercase; text-decoration-line:underline;">
    Vision
  </h3>
  <p>
     To reach out to vulnerable children in Uganda within all aspects of their lives.</p>
  <h3 class="mb-30" style="font-size:25px; text-transform:uppercase; text-decoration-line:underline;">
    Mission
  </h3>
  <p>
    <span style="font-weight:600; font-size:18px;">VKIDS Trust Uganda</span> is a Christian ministry who is dedicated to improving the quality of education, safety, and life of underprivilaged children in Kalangala and the surronding areas.<br />
    Our mission is accomplished by the support of dedicated people around the world who share the strong desire to make a positive difference in the life of vulnerable children.</p>
</div>

